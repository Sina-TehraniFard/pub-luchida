/** Set 化したルックアップ用 */
export const BOJ_MEETING_DATE_SET: ReadonlySet<string> = new Set(BOJ_MEETING_DATES);

/** UTC Date を JST 日付文字列 "YYYY-MM-DD" に変換 */
export function toJstDateString(utcDate: Date): string {
  const jst = new Date(utcDate.getTime() + 9 * 60 * 60 * 1000);
  return `${jst.getUTCFullYear()}-${String(jst.getUTCMonth() + 1).padStart(2, '0')}-${String(jst.getUTCDate()).padStart(2, '0')}`;
}

/**
 * 指定 UTC 日時が BOJ 会合日の ±daysMargin 日以内かを判定（JST 基準）。
 * daysMargin=0 なら当日のみ、1 なら ±1 日、2 なら ±2 日。
 */
export function isWithinBojMeeting(utcDate: Date, daysMargin: number): boolean {
  const jst = new Date(utcDate.getTime() + 9 * 60 * 60 * 1000);
  // JST 基準の「その日」を起点に ±daysMargin でチェック
  for (let offset = -daysMargin; offset <= daysMargin; offset++) {
    const check = new Date(jst.getTime() + offset * 24 * 60 * 60 * 1000);
    const key = `${check.getUTCFullYear()}-${String(check.getUTCMonth() + 1).padStart(2, '0')}-${String(check.getUTCDate()).padStart(2, '0')}`;
    if (BOJ_MEETING_DATE_SET.has(key)) return true;
  }
  return false;
}
